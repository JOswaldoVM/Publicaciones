    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        String titulo, ISSN; 
        int noPaginas, numero;
        double precio;
        try{
           titulo = txtTitulo.getText();
           precio = Double.parseDouble(txtPrecio.getText());
           noPaginas = Integer.valueOf(txtNoPaginas.getText());
           ISSN = txtISBN.getText();
           numero = Integer.valueOf(txtNumero.getText());
           

           mp = new ManPublica(titulo, precio, noPaginas);
           mp.alta(ISSN, numero);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Revise los datos de entrada", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

